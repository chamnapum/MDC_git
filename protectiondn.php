ARTICLE 2. DONNEES RELATIVES A LA CONSULTATION DU SITE



Certaines informations non personnelles peuvent être recueillies a l'occasion de votre navigation sur le Site, comme la version de votre navigateur (Chrome, Firefox, Safari, Opéra, Internet Explorer, etc.), le type de système d'exploitation utilisé (Linux, Windows 98, Mac Os, etc.) et l'adresse IP (Internet Protocol) de l'ordinateur utilisé.



La société magasinducoin utilise des cookies, destinés à stocker des informations pouvant identifier votre terminal (ordinateur, tablette, smartphone, etc.) durant la consultation du Site afin de faciliter l'expérience de navigation sur ce dernier.



En outre, la société magasinducoin est susceptible d'acheter des espaces publicitaires directement ou par l'intermédiaire de prestataires (agences de conseil en communication) afin de promouvoir ses activités et ses offres sur des sites ou applications de tiers, au moyen de contenus publicitaires (texte, graphismes, animations, vidéos, etc.) diffusés par ces sites ou applications. De même, des cookies sont susceptibles d'être inclus dans les espaces publicitaires du Site. Ces espaces publicitaires affichent sur votre terminal des contenus publicitaires émanant d'annonceurs. Ces espaces contribuent au financement des contenus et des services que la société magasinducoin met à votre disposition.



Lors de l'affichage de tels contenus publicitaires, des informations relatives à la navigation de votre terminal (ordinateur, tablette, smartphone, etc.) sont susceptibles d'être enregistrées dans des fichiers « cookies » installés sur ce terminal, sous réserve des choix exprimés concernant les cookies, par l'un des moyens décrit à l'Article 2.5 ci-dessous et que vous pouvez modifier a tout moment.



Seul l'émetteur d'un cookie est susceptible de lire ou de modifier des informations qui y sont contenues.



Les cookies que la société magasinducoin émet permettent :



    d'établir des statistiques et volumes de fréquentation et d'utilisation des divers éléments composant le Site (rubriques et contenus visités, parcours), et nous permettent d'améliorer l'intérêt et l'ergonomie des services consultés sur le Site,

    de comptabiliser le nombre total de publicités affichées par la société magasinducoin sur ses espaces publicitaires, d'identifier ces publicités, leur nombre d'affichages respectifs, le nombre d'utilisateurs ayant cliqué sur chaque publicité et, le cas échéant, les actions ultérieures effectuées par ces utilisateurs sur les pages auxquelles mènent ces publicités, afin de calculer les sommes dues aux acteurs de la chaîne de diffusion publicitaire (agence de communication, régie publicitaire, site/support de diffusion) et d'établir des statistiques,

    d'adapter la présentation du Site et des espaces publicitaires y afférent aux préférences d'affichage de votre terminal (langue utilisée, résolution d'affichage, système d'exploitation utilisé, etc) lors des visites sur le Site, selon les matériels et les logiciels de visualisation ou de lecture que ce terminal comporte,

    d'adapter les contenus publicitaires affichés sur votre terminal par les espaces publicitaires, selon la navigation de ce terminal et/ou en fonction des données personnelles que vous nous avez fournies,

    de mémoriser des informations relatives à un formulaire que vous avez rempli sur le Site (inscription ou accès au compte) ou à des produits, services ou informations que vous avez choisis sur le Site (service souscrit, contenu d'un panier de commande, etc.),

    de vous permettre d'accéder à des espaces réservés et personnels du Site, grâce aux identifiants choisis lors de l'inscription au Site,

    de mettre en œuvre des mesures de sécurité, par exemple lorsqu'un accès réservé est révoqué après un certain laps de temps.



Si vous refusez l'enregistrement de cookies sur votre terminal, ou en cas de suppression de ceux qui y sont enregistrés, vous ne pourrez plus bénéficier d'un certain nombre de fonctionnalités qui sont néanmoins nécessaires pour naviguer dans certains espaces du Site, en particulier, le panier de Commande. Le cas échéant, la société magasinducoin décline toute responsabilité pour les conséquences liées au fonctionnement dégradé des services du Site résultant de l'impossibilité d'enregistrer ou de consulter les cookies nécessaires à leur fonctionnement et que vous auriez refusés ou supprimés.

2.1. L'intérêt de voir s'afficher des publicités adaptées à la navigation



L'objectif de la société magasinducoin est de vous présenter les publicités les plus pertinentes possibles. A cette fin, la technologie des cookies permet de déterminer en temps réel quelle publicité afficher à un terminal, en fonction de sa navigation récente sur un(e) ou plusieurs sites ou applications.



Votre intérêt pour les contenus publicitaires qui sont affichés sur votre terminal lors de la consultation d'autres sites détermine souvent les ressources publicitaires de celui-ci, lui permettant d'exploiter ses services, souvent fournis aux utilisateurs à titre gratuit. Vous préférez sans doute voir s'afficher des publicités qui correspondent à ce qui vous intéresse plutôt que les publicités qui n'ont aucun intérêt pour vous. De même, les annonceurs qui souhaitent voir leurs publicités diffusées sont intéressés par l'affichage de leurs offres auprès d'utilisateurs susceptibles d'être les plus intéressés par celles-ci.

2.2. En cas d'utilisation partagée de votre terminal



Si votre terminal est utilisé par plusieurs personnes et lorsqu'un même terminal dispose de plusieurs logiciels de navigation, la société magasinducoin ne saurait s'assurer de manière certaine que les services et publicités destinés à ce terminal correspondent bien à votre utilisation propre et non a celle d'un autre utilisateur de ce terminal.



Le cas échéant, le partage avec d'autres personnes de l'utilisation de ce terminal et la configuration des paramètres du navigateur utilisé à l'égard des cookies, relèvent de votre libre choix et de votre responsabilité.

2.3. Cookies tiers



Lorsque vous naviguez sur le Site, des cookies émis par des sociétés tierces peuvent être placés par ces dernières sur votre terminal, sous réserve des choix que vous avez pu exercer antérieurement ou à tout moment, notamment au travers de la configuration des paramètres de votre logiciel de navigation, dans les conditions décrites à l'Article 2.5 ci-dessous. Ces cookies ont pour finalité d'identifier les Produits consultés sur le Site et de collecter des données de navigation afin de personnaliser l'offre publicitaire qui pourrait vous être présentée en dehors du Site. Dans le cadre de ce type de partenariat publicitaire, la société magasinducoin peut être amenée à transmettre au partenaire concerné des données de navigation anonymes concernant les Produits consultés avec votre terminal lors de votre navigation sur le Site.



L'émission et l'utilisation de cookies par des tiers sont soumises aux politiques de protection de la vie privée de ces tiers. La société magasinducoin vous informe de l'objet des cookies dont la société magasinducoin a connaissance et des moyens dont vous disposez pour effectuer des choix à l'égard de ces cookies et de leurs émetteurs respectifs.

2.3.1. Via des contenus de tiers diffusés dans nos espaces publicitaires



Les contenus publicitaires (graphismes, animations, vidéos, etc.) diffusés dans nos espaces publicitaires sont susceptibles de contenir des cookies émis par des tiers : soit l'annonceur a l'origine du contenu publicitaire concerné, soit une société tierce a l'annonceur (agence conseil en communication, société de mesure d'audience, prestataire de publicité ciblée, etc.), qui a associé un cookie au contenu publicitaire d'un annonceur.



Le cas échéant, les cookies émis par ces tiers peuvent leur permettre, pendant la durée de validité de ces cookies :



    de comptabiliser le nombre d'affichages des contenus publicitaires diffusés via nos espaces publicitaires, d'identifier les publicités ainsi affichées, le nombre d'utilisateurs ayant cliqué sur chaque publicité, leur permettant de calculer les sommes dues de ce fait et d'établir des statistiques,

    de reconnaître votre terminal lors de votre navigation ultérieure sur tout autre site ou service sur lequel ces annonceurs ou ces tiers émettent également des cookies et, le cas échéant, d'adapter ces sites et services tiers ou les publicités qu'ils diffusent, à la navigation de votre terminal dont ils peuvent avoir connaissance.



2.3.2. Par une régie publicitaire externe exploitant nos espaces publicitaires



Les espaces publicitaires de notre site sont susceptibles d'être exploités par une ou plusieurs régie(s) publicitaire(s) externe(s) et, le cas échéant, de contenir des cookies émis par l'une d'entre elles.



Le cas échéant, les cookies émis par ces régies publicitaires externes leur permettent, pendant la durée de validité de ces cookies :



    de comptabiliser le nombre total de publicités affichées par leurs soins sur nos espaces publicitaires, d'identifier ces publicités, leur nombre d'affichages respectifs, le nombre d'utilisateurs ayant cliqué sur chaque publicité et, le cas échéant, les actions ultérieures effectuées par ces utilisateurs sur les pages auxquelles mènent ces publicités, afin de calculer les sommes dues aux acteurs de la chaîne de diffusion publicitaire (annonceur, agence de communication, régie publicitaire, site/support de diffusion) et d'établir des statistiques,

    d'adapter les espaces publicitaires qu'elles opèrent aux préférences d'affichage de votre terminal (langue utilisée, résolution d'affichage, système d'exploitation utilisé, etc), selon les matériels et les logiciels de visualisation ou de lecture que votre terminal comporte,

    d'adapter les contenus publicitaires affichés sur votre terminal via nos espaces publicitaires selon la navigation de votre terminal sur notre site,

    d'adapter les contenus publicitaires affichés sur votre terminal via nos espaces publicitaires, selon la navigation antérieure ou ultérieure de votre terminal sur des sites de tiers au sein desquels la régie concernée émet également des cookies, sous réserve que ces cookies aient été enregistrés dans votre terminal conformément aux choix que vous avez exercés par l'un des moyens décrits a l'Article 2.5 ci-dessous,

    d'adapter les contenus publicitaires affichés sur votre terminal via nos espaces publicitaires en fonction des données de localisation (longitude et latitude) susceptibles d'être transmises, sous réserve de votre accord explicite et préalable, par votre terminal mobile,

    d'adapter les contenus publicitaires affichés sur votre terminal dans nos espaces publicitaires en fonction des données personnelles que vous auriez pu fournir à cette régie publicitaire.



2.4. Les cookies "Flash" de Adobe Flash Player



« Adobe Flash Player »™ est une application informatique qui permet le développement rapide des contenus dynamiques utilisant le langage informatique « Flash ». Flash (et les applications de même type) mémorise les paramètres, les préférences et l'utilisation de ces contenus grâce à une technologie similaire aux cookies. Toutefois, « Adobe Flash Player »™ gère ces informations et vos choix via une interface différente de celle fournie par votre logiciel de navigation.



Dans la mesure où votre terminal serait susceptible de visualiser des contenus développés avec le langage Flash, nous vous invitons à accéder à vos outils de gestion des cookies Flash, directement depuis le site http://www.adobe.com/fr/.

2.5. Comment exprimer vos choix concernant les cookies ?

2.5.1. Vos choix exprimés directement via votre terminal



Vous pouvez autoriser ou refuser l'enregistrement de cookies dans votre terminal avec les paramètres appropriés de votre logiciel de navigation sur Internet. Sur ce point, nous vous invitons à consulter les conseils d'utilisation de ces paramètres, selon le navigateur utilisé, accessibles sur la page du site internet de la Commission Nationale de l'Informatique et des Libertés : http://www.cnil.fr/vos-libertes/vos-traces/les-cookies/.



Vous pouvez également autoriser ou refuser l'enregistrement de cookies dans votre terminal (mais dans ce dernier cas, vous ne pourrez plus passer de commande sur le Site), en configurant votre navigateur à cette fin.



Attention, la configuration de chaque navigateur est différente. Elle est décrite dans le menu d'aide de votre navigateur, qui vous permettra de savoir de quelle manière modifier vos souhaits en matière de cookies, notamment de la maniere suivante pour les navigateurs les plus couramment utilisés :



    Pour Internet Explorer™ : http://windows.microsoft.com/fr-FR/windows-vista/Block-or-allow-cookies

    Pour Safari™ : http://www.apple.com/fr/privacy/use-of-cookies/

    Pour Chrome™ : http://support.google.com/chrome/bin/answer.py?hl=fr&hlrm=en&answer=95647

    Pour Firefox™ : http://support.mozilla.org/fr/kb/Activer%20et%20d%C3%A9sactiver%20les%20cookies

    Pour Opéra™ : http://help.opera.com/Windows/10.20/fr/cookies.html



2.5.2. Vos choix exprimés en ligne auprès de plateformes interprofessionnelles



Vous pouvez vous connecter au site Youronlinechoices, proposé par les professionnels de la publicité digitale regroupés au sein de l'association européenne EDAA (European Digital Advertising Alliance) et géré en France par l'Interactive Advertising Bureau (IAB) France.



Vous pourrez ainsi connaître les entreprises inscrites à cette plate-forme et qui vous offrent la possibilité d'accepter ou de refuser les cookies utilisés par ces entreprises pour adapter aux informations de navigation qu'elles traitent les publicités susceptibles d'être affichées lors de la consultation par votre terminal de services en ligne sur lesquels elles émettent des cookies : http://www.youronlinechoices.com/fr/controler-ses-cookies/.



Cette plate-forme européenne est partagée par des centaines de professionnels de la publicité sur Internet et constitue une interface centralisée vous permettant d'exprimer vos choix des cookies susceptibles d'être utilisés afin d'adapter à la navigation de votre terminal les publicités susceptibles d'être affichées lors de la visite d'un service en ligne. Notez que cette procédure n'empêchera pas l'affichage de publicités sur les sites Internet que vous visitez. Elle ne bloquera que les technologies qui permettent d'adapter des publicités a la navigation de votre terminal et à vos centres d'intérêts.

